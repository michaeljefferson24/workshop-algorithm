import React, { Component } from 'react';
import { Button } from 'react-bootstrap';


class Step extends Component {
  constructor(props) {
    super(props);

    this.state = {
      steps: [
        'get knife peanut-butter jelly and 2 slics of bread and place in the counter',
        'open the peanut butter to spread on 1 slice of bread',
        'open jelly to spread on the other slice of bread',
        'pick up both pieces of bread and put them together',
        'pick up sandwhich and eat to enjoy'
      ],
      index: 0,
      button: 'next'
   };
  }

  render() {
      return (
        <div>
          <div className="step-text">{this.state.steps[this.state.index]}</div>
          <Button
            bsStyle="success"
            bsSize="large"
            onClick={event => this.setState({ index: this.state.index + 1 })}>
            { this.state.button }
          </Button>
        </div>
      );
  }
}

export default Step;
